#define int long long
#define io ios_base::sync_with_stdio(false);cin.tie(NULL);cout.tie(NULL)
#define mod 1000000007
int32_t main(){
    io;
    int t=1;
    cin>>t;
    while(t--){
      int n;
      cin>>n;
      vector<int>v(n);
      for(int i=0;i<n;i++)
      cin>>v[i];
      if(n==1){
          cout<<"YES"<<endl;
          continue;
      }
      int prev=-1,sum=0;
      bool ok=true;
      for(int i=0;i<n;i++){
          if(v[i]>prev){
              sum+=v[i]-i;
              v[i]=prev+1;
              prev=v[i];
          }
          else{
              if((sum+v[i])<=prev){
                  ok=false;
                  break;
              }
              else{
                  sum-=(prev+1-v[i]);
                  v[i]=prev+1;
                  prev=v[i];
                  
              }
              
          }
          
      }
      if(ok==false)
      cout<<"NO"<<endl;
      else
      cout<<"YES"<<endl;
      
    }
      
}